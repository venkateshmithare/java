public class CarDetails02 {
    public static void method(byte b, int i) {
        System.out.println("Byte: " + b + ", Integer: " + i);
    }

    public static void method(short s, double d) {
        System.out.println("Short: " + s + ", Double: " + d);
    }

    public static void method(long l, char c) {
        System.out.println("Long: " + l + ", Character: " + c);
    }

    public static void method(float f, boolean bool) {
        System.out.println("Float: " + f + ", Boolean: " + bool);
    }

    public static void method(int i, long l) {
        System.out.println("Integer: " + i + ", Long: " + l);
    }

    public static void method(double d, byte b) {
        System.out.println("Double: " + d + ", Byte: " + b);
    }

    public static void method(char c, float f) {
        System.out.println("Character: " + c + ", Float: " + f);
    }

    public static void method(boolean bool, short s) {
        System.out.println("Boolean: " + bool + ", Short: " + s);
    }

    public static void method(long l, boolean bool) {
        System.out.println("Long: " + l + ", Boolean: " + bool);
    }

    public static void method(byte b, char c, double d) {
        System.out.println("Byte: " + b + ", Character: " + c + ", Double: " + d);
    }
}