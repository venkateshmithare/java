public class SwapInteger{
	public static void main(String[] args) {
		int a=10;
		int b=25;
		System.out.println("before swaping :"+a);
		System.out.println("before swaping :"+b);
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("after swaping :"+a);
		System.out.println("after swaping :"+b);
	}
	}
	

