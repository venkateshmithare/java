public class Zip {
    String brand;
    String material;
    String color;
    double length;
    double width;
    boolean isWaterproof;
    String zipType;
    boolean isReversible;
    String usageType;
    boolean hasLock;
    String countryOfOrigin;
    int warrantyPeriod;
    boolean isRustProof;
    String sliderMaterial;
    boolean isDurable;
    int toothCount;
    boolean isSelfRepairing;
    String closureMechanism;
    boolean isEcoFriendly;
    String applicationArea;

    public Zip(String brand, String material, String color, double length, double width, boolean isWaterproof,
               String zipType, boolean isReversible, String usageType, boolean hasLock, String countryOfOrigin,
               int warrantyPeriod, boolean isRustProof, String sliderMaterial, boolean isDurable,
               int toothCount, boolean isSelfRepairing, String closureMechanism, boolean isEcoFriendly,
               String applicationArea) {
        this.brand = brand;
        this.material = material;
        this.color = color;
        this.length = length;
        this.width = width;
        this.isWaterproof = isWaterproof;
        this.zipType = zipType;
        this.isReversible = isReversible;
        this.usageType = usageType;
        this.hasLock = hasLock;
        this.countryOfOrigin = countryOfOrigin;
        this.warrantyPeriod = warrantyPeriod;
        this.isRustProof = isRustProof;
        this.sliderMaterial = sliderMaterial;
        this.isDurable = isDurable;
        this.toothCount = toothCount;
        this.isSelfRepairing = isSelfRepairing;
        this.closureMechanism = closureMechanism;
        this.isEcoFriendly = isEcoFriendly;
        this.applicationArea = applicationArea;
    }
}
